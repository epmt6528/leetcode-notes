// <What is a "Linked List" in JS>
// A linked list is a linear data structure similar to an array.
// Each element (commonly called nodes) contains two items: the data stored and a link to the next node.
// The data can be any valid data type.
// Advantage: Nodes can easily be removed or added from a linked list without reorganizing the entire data structure. This is one advantage it has over arrays.
// Disadvantage: Search operations are slow in linked lists. Unlike arrays, random access of data elements is not allowed. Nodes are accessed sequentially starting from the first node.
// It uses more memory than arrays because of the storage of the pointers.
